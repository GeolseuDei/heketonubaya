package com.example.heketonubaya;

public class POJO_Puskesmas {
    private String id;
    private String kecamatan;
    private String nama_puskesmas;
    private String kode_puskesmas;
    private String kelurahan;
    private String alamat;
    private String user_id;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getKecamatan() {
        return kecamatan;
    }

    public void setKecamatan(String kecamatan) {
        this.kecamatan = kecamatan;
    }

    public String getNama_puskesmas() {
        return nama_puskesmas;
    }

    public void setNama_puskesmas(String nama_puskesmas) {
        this.nama_puskesmas = nama_puskesmas;
    }

    public String getKode_puskesmas() {
        return kode_puskesmas;
    }

    public void setKode_puskesmas(String kode_puskesmas) {
        this.kode_puskesmas = kode_puskesmas;
    }

    public String getKelurahan() {
        return kelurahan;
    }

    public void setKelurahan(String kelurahan) {
        this.kelurahan = kelurahan;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public String getUser_id() {
        return user_id;
    }

    public void setUser_id(String user_id) {
        this.user_id = user_id;
    }
}
