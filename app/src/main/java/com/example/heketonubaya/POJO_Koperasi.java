package com.example.heketonubaya;

public class POJO_Koperasi {
    private String id;
    private String nama_koperasi;
    private String kelompok_koperasi;
    private String nomor;
    private String tgl_dibangun;
    private String alamat;
    private String kelurahan;
    private String kecamatan;
    private String user_id;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNama_koperasi() {
        return nama_koperasi;
    }

    public void setNama_koperasi(String nama_koperasi) {
        this.nama_koperasi = nama_koperasi;
    }

    public String getKelompok_koperasi() {
        return kelompok_koperasi;
    }

    public void setKelompok_koperasi(String kelompok_koperasi) {
        this.kelompok_koperasi = kelompok_koperasi;
    }

    public String getNomor() {
        return nomor;
    }

    public void setNomor(String nomor) {
        this.nomor = nomor;
    }

    public String getTgl_dibangun() {
        return tgl_dibangun;
    }

    public void setTgl_dibangun(String tgl_dibangun) {
        this.tgl_dibangun = tgl_dibangun;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public String getKelurahan() {
        return kelurahan;
    }

    public void setKelurahan(String kelurahan) {
        this.kelurahan = kelurahan;
    }

    public String getKecamatan() {
        return kecamatan;
    }

    public void setKecamatan(String kecamatan) {
        this.kecamatan = kecamatan;
    }

    public String getUser_id() {
        return user_id;
    }

    public void setUser_id(String user_id) {
        this.user_id = user_id;
    }
}
